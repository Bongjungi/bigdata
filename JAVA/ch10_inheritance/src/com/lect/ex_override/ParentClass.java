package com.lect.ex_override;
//오버라이딩(함수 재정의: 상속 받는 클래스가 다시 재정의
//오버로딩(함수 중복정의: 매개변수의 타입이나 객수를 달리하여 같은 이름의 함수 중복해서 정의

//클래스는 틀을 만들어서 main으로 출력하는 걸 연습한 거고, 상속은 틀로 비슷한 틀을 만들어서 main함수로 출력하는 것을 연습하는 것이다. 
public class ParentClass {

	public void method1() {
		System.out.println("Parent의 method1");
		System.out.println();
	}
	public void method2() {
		System.out.println("Parent의 method2");
		System.out.println();
	}
	
	
	
}
