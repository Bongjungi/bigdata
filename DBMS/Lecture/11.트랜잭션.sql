--[XI] 트랜잭션 명령어(DML명령어) ; COMMIT, ROLLBACK, SAVEPOINT
COMMIT;
-- 새로운 트랜잭션 시작(INSERT, UPDATE, DELETE 작업 새로 추가될 예정)
CREATE TABLE DEPT011 AS SELECT * FROM DEPT;
DELETE FROM DEPT011 WHERE DEPTNO=40; --DB에서 삭제되지는 않았다.
ROLLBACK;
--새로운 트랜잭션 시작
SELECT * FROM DEPT011;
DELETE FROM DEPT011 WHERE DEPTNO = 40;
COMMIT;
--새로운 트랜잭션 시작
SELECT * FROM DEPT011; --10,20,30부서
DELETE FROM DEPT011 WHERE DEPTNO = 30;
SELECT * FROM DEPT011; --10,20부서
SAVEPOINT C1; -- C1지점 10,20
DELETE FROM DEPT011 WHERE DEPTNO = 20;
SELECT * FROM DEPT011; --10부서
SAVEPOINT C2; --C2지점 10
DELETE FROM DEPT011 WHERE DEPTNO = 10; -- 없음
SELECT * FROM DEPT011; --없음
ROLLBACK TO C2; -- C2지점으로 돌아감
SELECT * FROM DEPT011;--10부서  트랜잭션에 20지운거랑 30지운거 남아있다.
COMMIT;