package com.lec.ex;

public class VarEx01 {
	public static void main(String[] args) {
		int age=23;
		//타입 변수명=(할당연산자) 초기값;
		String name="쿠쿠";
		System.out.println("하이"+age+"살"+name+"님");
		//sysout 하고 컨트롤 스페이스 누르면 sysoutpriln자동이다.
		age+=1;
		System.out.println("하이"+age+"살\n"+name+"님");
		
	}
}
